2. <B>Фишки и изменения в новой версии по сравнению с v. 1.8 от 15.03.2009</B>: <BR><br>
- На форуме зарегистрированным юзерам <B>можно присоединять в ответе файл zip / rar / 7z</B> - формата. Функция отключается в админке;<br>
- При добавлении фото РЕПУТАЦИЯ прибавляется на 5 единиц;<br>
- При переносе темы из закрытого раздела - терялось ограничение по звёздам на раздел. ИСПРАВЛЕНО.<br>
- Когда создаёшь новую тему - нельзя пользоваться смайликами. Исправлено. Сделал единую форму для создания темы / ответа в теме (со смайлами, тэгами и прикреплением файлов);<br>
- Была проблема, например, при создании темы: 22 апреля 2009г. г.Санкт Петербург, концерт группы "Невский Портал". Ответить в теме нельзя. Ошибка исправлена.<br>
- Начаты разработки блока голосования для форума. Намётки есть, пожалуйста, тестируем голосование. Я готов выслушать предложения по функционированию блока.<br>
- В профиле участника сделана строка активности, например: Всего сообщений: 86 [4.46% от общего числа / 0.19 сообщений в сутки];<br>
