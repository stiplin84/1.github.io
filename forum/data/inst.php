- Для повышения безопасности форума добавил ini_set('register_globals','off'); в начало всех запускаемых php-блоков.<br>
- Сообщения при выводе на экран теперь фильтруются скриптом АНТИМАТА Дмитрия Перова (diktator_antimat v1.1_alfa). Спасибо ему огромное Есть нюансы - некоторые слова распознаются как маты: грёбля, грёбаный, глухую, ХЛЕБАЛО, ХЛЕБАЛЬНИК, хулиган... и некоторые другие. Функция можно регулировать в index.php - $antimat=1; // включить АНТИМАТ да/нет - 1/0<br>
- Готова в сообщениях форума заветная для некоторых кнопочку "Ответить на сообщение".<br>
- Система сохранения смайлов пересмотрена, изменена на более продвинутую.<br>
- Имелась проблема: если удалить новую тему, то на главной она продолжает отображаться и в разделе "Обновления" и в разделе "Последние сообщения".  - ИСПРАВЛЕНО.<br>
- Правила форума вынесены в отдельный файл - pravila.html.<br>
- доработал блок поверки корректности расширения файла - ранее он работал не верно;<br>
- Теперь можно прикреплять файлы с графическим расширением: jpg,jpeg,bmp,gif,png и эти файлы автоматически будут показываться во весь экран.<br>
- В админке в участниках "съезжал" блок удаления, подправил, выравнил.<br>
- БАГ: Репутация за каждое сообщение по 6 баллов прибавляет почему то... ИСПРАВЛЕН.В index.php в самом верху вынесена переменная - по сколько очков добавлять к репутации за загрузку файла? По умолчанию - 5. Тамже добавлены переменные - репа за добавление сообщения и темы.<br>
- доработан код скрипта для корректного функционирования с php 5. Теперь скрипт работает с php начиная версией 4.3.6 и заканчивая версией 5.3.0.<br>
