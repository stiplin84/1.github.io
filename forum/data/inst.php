1. Универсальная панель администратора с возможностями:<BR>
- <B>выбора своего заголовка</B> для всех страниц (<B>увеличивает рейтинг</B> форума в поисковых системах);<BR>
- <B>выбора краткого описания форума</B> для всех страниц (<B>увеличивает рейтинг</B> форума и отображается при поиске в качестве описания);<BR>
- выбор <B>количества отображаемых тем / сообщенией на каждой странице</B>;<BR>
- возможность <B>перемещения разделов / топиков, а также редактирования и удаления тем / ссобщений</B>.<BR>
- <B>наличие модерпанели</B>! Объеденина с админкой. Вход осуществляется со страницы admin.php логины такие: администратор или модератор. Пароль админа по умолчанию - admin. У модера нет пароля - смените в админке. Админ может всё, модер всё, кроме изменения настроек форума и редктирования/удаления/перемещения разделов на главной форума;<br>
- имеется <B> поиск сообщений</B>; <BR>
- <B>возможность регистрации/просмотра/редактирования профиля</B> участниками; <BR>
- доступна <B> загрузка аватаров.</B> Пользователь может загрузить аватар со своего компьютера (только);<BR>
- возможность <B>быстрого перехода</B> с главной страницы <B>на последнюю страницу новой</B> темы;<BR>
- в заголовке и титуле страницы отображается строка типа <B>ТЕМА -> РАЗДЕЛ -> ФОРУМ</B> - что удобно для навигации по форуму и хорошо для поисковиков;<BR>
- показ <B>новых сообщений в темах</B> форума (все сообщения после вашего последнего визита выделяются);<BR>
- <B>тэг [img][/img].</B> Позволяет отображать любые картинки, расположенные в Интернете. Необходимо указать урл, например: <B>[img]http://ya.ru/ya.gif[/img]</B>;<BR>
- все файлы данных хранятся в одной папке. По умолчанию - "<B>./data</B>". Содержимое папки защищено <B>от взлома файлом .htaccess.</B><BR>
- генерация <B>странички-статистики по всем пользователям по N-человек, а не всем списком</B>. Выбирается этот параметр в админке;<BR>
- возможно каждому <B>участнику давать кликуху</B> (личный статус пользователя) типа "WEB-мастер", "Болтун" и др. Смотрите в админинтерфейсе;<BR>
- автозамена адреса типа http://www.gamesmania.ru/ на <a href="http://www.gamesmania.ru/" target="_blank">http://www.gamesmania.ru/</a>;<BR>
- автозамена тегов на их символьные аналоги;<BR>
- автозамена смайликов на картинки</B> (в состав архива входят 11 смайликов:
